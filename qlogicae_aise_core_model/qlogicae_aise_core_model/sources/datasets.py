

def escape_braces(template):
    result = ""
    i = 0
    while i < len(template):
        if template[i:i+2] == "{}":
            result += "{}"
            i += 2
        elif template[i] == "{":
            result += "{{"
            i += 1
        elif template[i] == "}":
            result += "}}"
            i += 1
        else:
            result += template[i]
            i += 1

    return result


